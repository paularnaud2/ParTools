WITH CTR_FRN AS (
    SELECT DISTINCT ctr.CTR_T_NUM CONTRAT, acm.ACM_T_LIB FOURNISSEUR
    FROM CONTRAT.ASS_CTR_TAM_ACM acta
    JOIN CONTRAT.ACTEUR_MARCHE acm ON acm.ACM_ID = acta.ACM_ID
    JOIN CONTRAT.TYPE_ACTEUR tam ON tam.TAM_ID = acta.TAM_ID
    JOIN CONTRAT.CONTRAT ctr ON ctr.CTR_ID = acta.CTR_ID
    WHERE 1=1
    AND tam.TAM_C_CODE LIKE 'F%'
    )

SELECT prm.PRM_ID POINT
, prm.PRM_AI_CODE_POSTAL CODE_POSTAL
, SUBSTR(prm.PRM_AIN_LIGNE6, 7) as COMMUNE
, DECODE(situ.SCN_APP_APPLICATION_CODE
	, 'QETGC', 'DISCO'
	, situ.SCN_APP_APPLICATION_CODE) as SI
, DECODE(scm.SCM_DC_STRUCTURE_COMPTAGE_CODE
	, 'AMM', 'LINKY'
	, 'CBE') COMPTEUR
--, scm.SCM_DC_COMPTEUR_COMMUNICANT COMMUNICANT, prm.PRM_DG_NIV_OUV_SERV NIV_OUV
, cf.FOURNISSEUR
, situ.SCN_CF_CATEGORIE_CODE as CAT_CLIENT
FROM SGEL_PRM_SCH.T_PRM prm
JOIN SGEL_PRM_SCH.T_SITUATION_CONTRACTUELLE situ ON prm.SCN_ID = situ.SCN_ID
JOIN CTR_FRN cf ON situ.SCN_CF_NUMERO_CONTRAT = cf.CONTRAT
JOIN SGEL_PRM_SCH.T_SITUATION_COMPTAGE scm ON scm.SCM_ID = prm.SCM_ID
WHERE 1=1
AND prm.PRM_SC_SEGMENT = 'C5'
AND prm.PRM_DG_ETAT_CODE = 'ACTI'
AND prm.PRM_SC_ETAT_CONTRACTUEL_CODE = 'SERVC'
AND (prm.PRM_DG_NIV_OUV_SERV IS NULL OR prm.PRM_DG_NIV_OUV_SERV = '0')
AND (scm.SCM_DC_COMPTEUR_COMMUNICANT IS NULL OR scm.SCM_DC_COMPTEUR_COMMUNICANT = '0')
AND (
prm.PRM_ID LIKE '191%'
OR prm.PRM_AI_CODE_POSTAL IN ('22580', '22200', '22490', '22100', '22370', '22290', '22520', '22250', '22140', '22240', '22650', '22130', '22410', '22380', '22980', '22260', '22330', '22770', '22400', '22750', '22430', '22350', '22550', '22630', '22680', '22860', '22830', '22500', '22930', '22690')
)
